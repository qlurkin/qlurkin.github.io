def write_maze():
    for i, line in enumerate(maze):
        stge.write_at(0, i, line)


def setup():
    return 0, 1


def loop(state):
    x, y = state
    xn, yn = x, y

    for k in stge.keypresses():
        if k == "RIGHT":
            xn += 1
        if k == "LEFT":
            xn -= 1
        if k == "UP":
            yn -= 1
        if k == "DOWN":
            yn += 1
        if k == "q":
            stge.quit()

    if maze[yn][xn] == " ":
        x, y = xn, yn

    write_maze()
    stge.write_at(x, y, "ጰ", fg=(255, 0, 0))

    return x, y


stge.run(setup, loop)
